$("#go").click("go", function () {
  $(".imgdiv").css("z-index", 100);
  $(".imgdiv").removeClass("animate__animated animate__bounceInUp");
  $(".imgdiv:nth-child(1)")
    .css("z-index", 200)
    .addClass("animate__animated animate__bounceInUp");
  $(".logo>ul>li>img").removeClass("actived");
  $(".logo>ul>li:nth-child(1)>img").addClass("actived");
  $("[name='ehide']").show();
});
